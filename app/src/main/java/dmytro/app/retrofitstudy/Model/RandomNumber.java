package dmytro.app.retrofitstudy.Model;

public class RandomNumber {
    private String text;
    private int number;
    private boolean found;
    private String type;

    public String getText() {
        return text;
    }

    public int getNumber() {
        return number;
    }

    public boolean isFound() {
        return found;
    }

    public String getType() {
        return type;
    }
}
